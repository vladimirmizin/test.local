  function store(Request $request)
    {
        $this->validate($request, [
            'title' => 'required|min:3|string',
            'body' => 'required',
        ]);

        $input['from_user'] = $request->user()->id;
        $input['title'] = $request->get('title');
        $input['body'] = $request->get('body');

        Comment::create( $input );
        return back();
    }

